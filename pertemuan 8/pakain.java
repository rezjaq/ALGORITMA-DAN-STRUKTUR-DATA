class pakain{
    String warna, jenis, merk, ukuran;
    double harga;

    pakain(String jenis, String warna, String merk, String ukuran, double harga){
        this.jenis = jenis;
        this.warna = warna;
        this.merk = merk;
        this.ukuran = ukuran;
        this.harga = harga;
    }
    
}